import { evaluate } from 'langsmith/evaluation';
import { ChatGoogleGenerativeAI } from '@langchain/google-genai';
import type { Job } from '@apps/api/src/app/jobs/types';
import { PromptTemplate } from '@langchain/core/prompts';
import { z } from 'zod';
import { DATASET_NAME, EXPERIMENT_PREFIX } from './constants';
import { env } from '../utils/env';
import { createOllamaAgentGraph } from '../utils/agent-utils';
import { createLogger } from '@apps/shared';

const logger = createLogger('graph-gemma-eval');

interface EvalInput {
  cvText: string;
  maxAppliedJobs: number;
  jobs: Job[];
}

interface EvalOutput {
  coverLetters: Array<{
    url: string;
    coverLetter: string;
  }>;
}

const MODELS = ['gemma4:e2b', 'gemma4:e4b', 'gemma3:12b'];

const EVALUATION_PROMPT = `You are an HR evaluating the cover letter of the candidate for the job position.

You will be given the CV of the candidate, the job description, and the cover letter.

Here is the grade criteria to follow:
(1) Grade the cover letter based ONLY on its relevance to the job description.
(2) Ensure that the most relevant information is highlighted.
(3) Ensure that there are no imaginary information.
(4) Ensure that the cover letter is well-structured, easy to read and not too long (over 500 words).
(5) Ensure that the cover letter sounds natural and not written by AI.

Explain your reasoning in a step-by-step manner to ensure your reasoning and conclusion are correct.

Grade from 0 to 1, where 0 is none of the criteria is fulfilled and 1 is all of the criteria is the cover letter is perfect and you wouldn't change anything.

CV: <cv>{cvText}</cv>

Job Description: <job_description>{jobDescription}</job_description>

Cover Letter: <cover_letter>{coverLetter}</cover_letter>
`;

const grade = z
  .object({
    reasoning: z
      .string()
      .describe('Explain your reasoning for why you chose this grade.'),
    grade: z.number().describe('Grade from 0 to 1.'),
  })
  .describe(
    'Evaluate the quality of the cover letters generated by the agent.',
  );

const graderLlm = new ChatGoogleGenerativeAI({
  model: env.GRADER_LLM_MODEL,
  apiKey: env.GEMINI_API_KEY,
}).withStructuredOutput(grade);

async function correctnessEvaluator({
  inputs,
  outputs,
}: {
  inputs: Record<string, any>;
  outputs: Record<string, any>;
}) {
  const cvText = inputs.cvText;
  const jobs = inputs.jobs;
  const coverLetters = outputs?.coverLetters;
  const coverLetter = coverLetters?.[0]?.coverLetter;

  const prompt = await PromptTemplate.fromTemplate(EVALUATION_PROMPT).invoke({
    cvText,
    jobDescription: jobs[0].description,
    coverLetter,
  });

  const grade = await graderLlm.invoke(prompt);
  const score = grade.grade;
  return { key: 'correctness', score: Number(score), comment: grade.reasoning };
}

async function runGraph(inputs: EvalInput, model: string): Promise<EvalOutput> {
  const { graph, cleanup } = await createOllamaAgentGraph(model);

  try {
    const answer = await graph.invoke(
      {
        cvText: inputs.cvText,
        maxAppliedJobs: inputs.maxAppliedJobs,
        jobs: inputs.jobs,
      },
      {
        configurable: { thread_id: '1' },
        recursionLimit: 4 * inputs.jobs.length + 5,
      },
    );
    return { coverLetters: answer.coverLetters };
  } finally {
    await cleanup();
  }
}

async function main() {
  for (const model of MODELS) {
    const results = await evaluate(
      async (inputs: EvalInput) => {
        return await runGraph(inputs, model);
      },
      {
        data: DATASET_NAME,
        evaluators: [correctnessEvaluator],
        experimentPrefix: EXPERIMENT_PREFIX + model,
        maxConcurrency: 2,
        metadata: { models: [model] },
      },
    );
    logger.info(results);
  }
}

if (require.main === module) {
  main().catch((e) => {
    logger.error(e);
    process.exit(1);
  });
}
